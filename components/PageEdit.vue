<template>
  <footer class="page-edit">
    <div v-if="editLink" class="edit-link">
      <a :href="editLink" target="_blank" rel="noopener noreferrer">{{ editLinkText }}</a>
      <OutboundLink />
    </div>

    <div style="float: right">
      <small ref="counter" class="word-count">
        <small>
          <span class="octicon octicon-file" />
        </small>
        <span id="word" />
        <small>
          <span class="octicon octicon-clock" />
        </small>
        <span id="time" />
      </small>

      <small :id="slug" class="leancloud-visitors">
        <span class="prefix">
          <small>
            <span class="octicon octicon-eye" />
          </small>
        </span>
        <span class="pv leancloud-visitors-count" />
      </small>

      <!-- We don't need last update section -->
      <!-- <div v-if="lastUpdated" class="last-updated">
        <span class="prefix">{{ lastUpdatedText }}:</span>
        <span class="time">{{ lastUpdated }}</span>
      </div> -->
    </div>
  </footer>
</template>

<script>
import isNil from 'lodash/isNil'
import { endingSlashRE, outboundRE, wordcount, min2read } from '../util'

export default {
  name: 'PageEdit',

  computed: {
    slug() {
      return this.$page.path || this.$route.path
    },

    lastUpdated() {
      return this.$page.lastUpdated
    },

    lastUpdatedText() {
      if (typeof this.$themeLocaleConfig.lastUpdated === 'string') {
        return this.$themeLocaleConfig.lastUpdated
      }
      if (typeof this.$site.themeConfig.lastUpdated === 'string') {
        return this.$site.themeConfig.lastUpdated
      }
      return 'Last Updated'
    },

    editLink() {
      const showEditLink = isNil(this.$page.frontmatter.editLink)
        ? this.$site.themeConfig.editLinks
        : this.$page.frontmatter.editLink

      const { repo, docsDir = '', docsBranch = 'master', docsRepo = repo } = this.$site.themeConfig

      if (showEditLink && docsRepo && this.$page.relativePath) {
        return this.createEditLink(repo, docsRepo, docsDir, docsBranch, this.$page.relativePath)
      }
      return null
    },

    editLinkText() {
      return (
        this.$themeLocaleConfig.editLinkText ||
        this.$site.themeConfig.editLinkText ||
        `Edit this page`
      )
    }
  },

  mounted() {
    this.$forceUpdate()
  },

  updated() {
    const { $refs: parent$$refs } = this.$parent
    const { $el: $elContent } = parent$$refs.content
    const content = $elContent.innerText
    this.calculateWordAndReadTime(content)
  },

  methods: {
    createEditLink(repo, docsRepo, docsDir, docsBranch, path) {
      const bitbucket = /bitbucket.org/
      if (bitbucket.test(docsRepo)) {
        const base = docsRepo
        return (
          base.replace(endingSlashRE, '') +
          `/src` +
          `/${docsBranch}/` +
          (docsDir ? docsDir.replace(endingSlashRE, '') + '/' : '') +
          path +
          `?mode=edit&spa=0&at=${docsBranch}&fileviewer=file-view-default`
        )
      }

      const gitlab = /gitlab.com/
      if (gitlab.test(docsRepo)) {
        const base = docsRepo
        return (
          base.replace(endingSlashRE, '') +
          `/-/edit` +
          `/${docsBranch}/` +
          (docsDir ? docsDir.replace(endingSlashRE, '') + '/' : '') +
          path
        )
      }

      const base = outboundRE.test(docsRepo) ? docsRepo : `https://github.com/${docsRepo}`
      return (
        base.replace(endingSlashRE, '') +
        '/edit' +
        `/${docsBranch}/` +
        (docsDir ? docsDir.replace(endingSlashRE, '') + '/' : '') +
        path
      )
    },

    calculateWordAndReadTime(content) {
      const $elCounter = this.$refs.counter
      const $elWord = $elCounter.querySelector('#word')
      const $elTime = $elCounter.querySelector('#time')
      const word = wordcount(content)
      const time = min2read(content)
      $elWord.innerHTML = word
      $elTime.innerHTML = `${time}&nbsp;分钟`
    }
  }
}
</script>

